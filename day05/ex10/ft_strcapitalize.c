char	*ft_strcapitalize(char *str)
{
	int	i;
	int a;

	i = 0;
	a = 0;
	if (str[i] > 96 && str[i] < 123)
	{
		str[i] = str[i] - 32;
		i++;
	}
	while (str[i] != '\0')
	{
		if ((str[i] < 97 || str[i] > 122))
		{
			a = 1;
		}
		else if (a == 1)
		{
			str[i] = str[i] - 32;
			a = 0;
		}
		i++;
	}
	return (str);
}
