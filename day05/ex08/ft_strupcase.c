char	*ft_strupcase(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] > 122 || str[i] < 97)
		{
			i++;
			continue ;
		}
		if (str[i] < 65 || str[i] > 90)
		{
			i++;
			continue ;
		}
		str[i] = str[i] - 32;
	}
	return (str);
}
